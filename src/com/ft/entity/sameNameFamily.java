package com.ft.entity;

public class sameNameFamily {
	private String name;
	private String fatherName;
	private String birthday;
	private String fatherBirthday;
	private String sex;
	private String nativePlace;
	private int familyId;
	
	public int getFamilyId() {
		return familyId;
	}
	public void setFamilyId(int familyId) {
		this.familyId = familyId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFatherName() {
		return fatherName;
	}
	public void setFatherName(String fatherName) {
		this.fatherName = fatherName;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getFatherBirthday() {
		return fatherBirthday;
	}
	public void setFatherBirthday(String fatherBirthday) {
		this.fatherBirthday = fatherBirthday;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getNativePlace() {
		return nativePlace;
	}
	public void setNativePlace(String nativePlace) {
		this.nativePlace = nativePlace;
	}
	

}
